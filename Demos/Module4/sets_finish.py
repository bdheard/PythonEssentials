david_courses = {"History", "Music", "Arts", "Literature", "Calculus"}
james_courses = {"Biology", "Chemistry", "Physics", "Statistics", "Calculus"}

# intersection
print(david_courses.intersection(james_courses))

# difference
print(david_courses.difference(james_courses))

# union
print(david_courses.union(james_courses))
